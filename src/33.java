public class ScientificCalculator {
    public static void main(String[] args) {
        double num1 = 5.0;
        double num2 = 3.0;

        System.out.println("The sum of " + num1 + " and " + num2 + " is: " + (num1 + num2));
        System.out.println("The difference between " + num1 + " and " + num2 + " is: " + (num1 - num2));
    }
}
